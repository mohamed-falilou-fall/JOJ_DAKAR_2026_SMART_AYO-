import pandas as pd
import numpy as np
from sklearn.linear_model import LinearRegression


def generer_donnees_historiques():

    sites=[
    "Dakar",
    "Diamniadio",
    "Saly"
    ]

    data=[]

    for site in sites:

        for heure in range(8,23):

            base=np.random.randint(2000,15000)

            data.append({
            "Zone":site,
            "Heure":heure,
            "Flux":base
            })

    return pd.DataFrame(data)


def entrainer_modele(df):

    X=df[["Heure"]]
    y=df["Flux"]

    model=LinearRegression()

    model.fit(X,y)

    return model


def prediction_journee(model,zone):

    heures=list(range(8,23))

    X=pd.DataFrame({"Heure":heures})

    pred=model.predict(X)

    result=pd.DataFrame({
    "Zone":zone,
    "Heure":heures,
    "Flux_prevu":pred.astype(int)
    })

    return result